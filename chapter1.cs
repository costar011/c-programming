        {
            /*
            statement: 문장은 프로그래밍에서 실행할 수 있는 최소 단위의 코드

            키워드: 특별한 의미가 부여된 단어

            식별자: 이름을 붙일 때에 사용하는 단어
            (특수 문자는 _ 만 허용, 숫자로 사용X, 키워드 사용X, 공백 입력X)
            클래스, 속성, 메소드, 네임스페이스 이름은 항상 `대문자`로 시작
            지역, 전역 변수 이름 항상 `소문자`로 시작
            여러 단어로 이루어진 식별자는 각 단어의 첫 글자를 `대문자`로 함
            Ex) 
            i love you -> iLoveYou , i am a boy -> iAmABoy

            메소드: C#에서 식별자 뒤에 괄호가 있으면 메소드라고 함 이 이외의 것은 변수

            주석: 코드 설명
            Ex) 한 줄 : // , 여러 줄 : /* /

            매개변수(파라미터): 메소드 괄호 안에 넣는 것
            Ex) Floor(10,1)

            규칙 지켜야 `유지보수` 가능 

             */


